#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	res;
	int	i;
	int	sinal;

	sinal = 1;
	res = 0;
	i = 0;
	while ((nptr[i] > 8 && nptr[i] < 14) || (nptr[i] == ' '))
	{
		i++;
	}
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
		{
			sinal = sinal * -1;
		}
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + nptr[i] - '0';
		i++;
	}
	return (res * sinal);
}
